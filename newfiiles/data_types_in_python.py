#data types in python

#number data type
integer=10
print(integer)
print(type(integer))

#complex data typ
complex_num=2+3j
print(complex_num)
print(type(complex_num))

#tuple data type
tup=(1,2,-1,3.5,4+5j)
print(tup)
print(type(tup))


#list data type
my_list=[1,2,-1,3.5,4+5j]
print(my_list)
print(type(my_list))

#dictionary data type
my_dict={"name":"Alice","age":30,"city":"New York"}
print(my_dict)
print(type(my_dict))

#string data type
my_string="Hello, World!"
print(my_string)    
print(type(my_string))

#set data type
my_set={1,2,3,4,5}
print(my_set)
print(type(my_set))

#boolean data type
is_true=True
print(is_true)
print(type(is_true))
