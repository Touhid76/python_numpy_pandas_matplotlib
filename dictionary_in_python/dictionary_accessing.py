#dictionary accessing in python
st={"name":'tafsir','roll':1,'id':12345}
#accessing value using key
print(st['name'])

#accessing value using get method
print(st.get('roll'))

print(len(st))

#to see keys in dictionary
print(st.keys())

#to see values in dictionary
print(st.values())

#to see items in dictionary
print(st.items())
