#arithmatic operation in python

a=10
b=20

#addition
add=a+b
print("addition:",add)

#subtraction
sub=a-b
print("subtraction:",sub)

#multiplication
mul=a*b
print("multiplication:",mul)

#division
div=a/b
print("division:",div)

#floor division
floor_div=a//b
print("floor division:",floor_div)

#modulus
mod=a%b
print("modulus:",mod)

#exponentiation
exp=a**b
print("exponentiation:",exp)
