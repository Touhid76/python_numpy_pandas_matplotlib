#set method in python
s={1,2,3,4,5}
s.add(6)
print(s)

#use of update method
c={7,8,9}
s.update(c)
print(s)

#use of discard method
s.discard(4)
print(s)

#use of remove method
s.remove(5)
print(s)

#use of pop method
s.pop()
print(s)

#use of copy method
s1=s.copy()
print(s1)

#use of clear method
s1.clear()
print(s1)